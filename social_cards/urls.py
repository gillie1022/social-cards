from django.contrib import admin
from django.conf import settings
from django.urls import include, path
from rest_framework import routers
from rest_framework.authtoken import views as authtoken_views
from cards import views as cards_views

router = routers.DefaultRouter()
router.register('users', cards_views.UserViewSet)
router.register('cards', cards_views.CardViewSet, basename='card')


urlpatterns = [
    path('admin/', admin.site.urls),
    path('api-auth/', include('rest_framework.urls')),
    path('api/auth/', include('djoser.urls')),
    path('api/auth/', include('djoser.urls.authtoken')),
    path('api/', include(router.urls)),
    path('api/follows/', cards_views.UserFollowsView.as_view(), name='follows')
]

